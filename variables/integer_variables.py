# Adding up using integer variables
# How many jelly beans do I have?

sour_cherry = 4
melon = 2
apple_pie = 5
coconut = 1

jelly_bean_total = sour_cherry + melon + apple_pie + coconut
print("You have", jelly_bean_total, "jelly beans remaining")
print(f"You have {jelly_bean_total} jelly beans remaining")
